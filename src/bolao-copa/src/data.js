// Lógica de pontuação corrigida (Máximo de 6 pontos por partida)
export function calcularPontosJogo(palpiteA, palpiteB, realA, realB) {
  if (
    realA === null ||
    realB === null ||
    palpiteA === undefined ||
    palpiteB === undefined ||
    palpiteA === "" ||
    palpiteB === ""
  ) {
    return 0;
  }

  const pA = parseInt(palpiteA, 10);
  const pB = parseInt(palpiteB, 10);
  const rA = parseInt(realA, 10);
  const rB = parseInt(realB, 10);

  if (pA === rA && pB === rB) return 6;

  let pontos = 0;
  if ((pA > pB && rA > rB) || (pB > pA && rB > rA) || (pA === pB && rA === rB))
    pontos += 3;
  if (pA === rA) pontos += 1;
  if (pB === rB) pontos += 1;

  return pontos;
}
